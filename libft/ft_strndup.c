#include "libft.h"

char	*ft_strndup(const char *src, int len)
{
	char	*dest;
	int		i;

	i = 0;
	dest = malloc((len + 1) * sizeof(char));
	if (!dest || len > (int)ft_strlen(src))
		return (NULL);
	while (src[i] && i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}